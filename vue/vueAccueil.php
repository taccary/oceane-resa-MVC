
<h1>Site de reservations des traversées de la Compagnie Océane</h1>
<p>	La compagnie Océane est une compagnie maritime implantée sur la côte ouest de la France qui assure la desserte maritime d’îles du littoral français par délégation de service public. </p>

<p>Parmi les îles desservies à partir du continent, on trouve, par exemple, Belle-Île-en-mer, Houat, Ile de Groix, Ouessant, Molène, Sein, Bréhat, Batz, Aix ou encore Yeu. </p>

<p>Ce site propose aux internautes les services suivants : 
<ul>
	<li>consultation des liaisons, </li>
	<li>consultation des tarifs, </li>
	<li>consultation des horaires des traversées, </li>
	<li>réservation en ligne. </li>
</ul>
</p>
